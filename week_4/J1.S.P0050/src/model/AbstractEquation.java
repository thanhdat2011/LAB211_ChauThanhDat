package model;

import java.util.List;

public abstract class AbstractEquation {
    protected void printList(List<Float> numbers) {
        for (int i = 0; i < numbers.size(); i++) {
            System.out.print(numbers.get(i));
            if (i < numbers.size() - 1) {
                System.out.print(", ");
            }
        }
        System.out.println();
    }
}
